function change_tyres()
{
	document.getElementById('tyres').className='tyres_over';
	document.getElementById('disks').className='disks_over';
	document.getElementById('tyres_td').className='tyres_td_over';
	document.getElementById('disks_td').className='disks_td_over';
}


function change_disks()
{
	document.getElementById('tyres').className='tyres';
	document.getElementById('disks').className='disks';
	document.getElementById('tyres_td').className='tyres_td';
	document.getElementById('disks_td').className='disks_td';
}